//Widening Typecasting
package com.jspiders.Typecasting;
public class Mainclass {
	public static void main(String[] args) {
		int a=10;
		float b=a;//int-->float
		System.out.println(b);
		long v=12345L;
		double d=v;//long-->double
		System.out.println(d);
		char c='A';
		int x=c;//char-->int
		System.out.println(x);
	}
}
