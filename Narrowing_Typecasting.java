//Narrowing Typecasting
package com.Typecasting;
public class Mainclass {
	public static void main(String[] args) {
		float a=4.5f;
		int b=(int)a;//Narrowing :float---->int
		System.out.println(b);
		int x=97;
		char y=(char)x;
		System.out.println(y);
		long p=3456789L;
		int q=(int)p;
		System.out.println(q);
	}
}
